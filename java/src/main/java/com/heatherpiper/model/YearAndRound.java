package com.heatherpiper.model;

public class YearAndRound {
    private int year;
    private int round;

    public YearAndRound() {
    }

    public YearAndRound(int year, int round) {
        this.year = year;
        this.round = round;
    }

    public int getYear() {
        return year;
    }

    public int getRound() {
        return round;
    }
}
